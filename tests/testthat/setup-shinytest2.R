    output_value_is_present <- function(value) {
      if (is.null(value) || length(value) == 0) {
        return(FALSE)
      }
      if (is.character(value)) {
        return(any(nzchar(value)))
      }
      if (is.list(value)) {
        return(length(value) > 0)
      }
      TRUE
    }
